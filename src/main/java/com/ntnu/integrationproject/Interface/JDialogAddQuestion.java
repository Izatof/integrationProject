    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        List<String> s=new ArrayList<String>();        
        Map<String,Object> map= new HashMap();
        if("".equals(jTextField2.getText()))
        {
            showMessageDialog(null,"You have to write a Question's title.");
        }
        else
        {
            if(res.contains(jTextField2.getText())&&!(edit))
            {
                showMessageDialog(null,"This Question's title is already used.");
            }
            else{
                if("".equals(jTextArea1.getText())||"".equals(jTextField1.getText())){
                    showMessageDialog(null,"All fields have to be filled");
                }
                else{
                    Map<String,Object> tmp= new HashMap();
                    tmp.put("_id", jTextField2.getText());
                    tmp.put("Question", jTextArea1.getText());
                    for(Component c : jPanel2.getComponents()){
                        if(c instanceof JTextField){
                            if(!((JTextField) c).getText().equals("")){
                                s.add(((JTextField) c).getText());
                            }                                
                        }
                    }
                    tmp.put("Expected",s);
                    tmp.put("Topic",jComboBox1.getSelectedItem().toString());
                    s=new ArrayList<String>();
                    for(Component c : jPanel3.getComponents()){
                        if(c instanceof JTextField){
                            if(!((JTextField) c).getText().equals("")){
                                s.add(((JTextField) c).getText());
                            }                                
                        }
                    }
                    tmp.put("Proposal", s);
                    if(edit){
                        System.out.println(edit);
                        questions.update(title,tmp);
                    }
                    else{
                        questions.insert(tmp);
                    }                    
                    new JDialog123 (parent1, false).setVisible(true);
                    this.setVisible(false);
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

